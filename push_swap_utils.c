#include "push_swap.h"

int	ft_atoi(const char *str)
{
	size_t		i;
	long int	sign;
	long int	num;

	i = 0;
	sign = 1;
	num = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		num += (int)str[i] - '0';
		num *= 10;
		i++;
	}
	return (sign * (int)(num / 10));
}


